            <h2 class="mb-3">Men and wellness: How to truly make an impact</h2>
              <span>08 December 2020</span>
              <p>
              <img src="images/blog 1.jpg" alt="" class="img-fluid">
            </p>
             <h5> Amid the current marketing messages used to communicate with men in the health and wellness space, a shift is taking place that is likely to see brands embrace and adopt new approaches to improve these conversations.</h5>
            <p>Research on health management trends​ published in January 2019 by market intelligence agency Mintel, reveals that males in the 18- to 34-year-old demographic were more likely to have suffered from stress, anxiety and depression compared to females, over the past year.</p>
              
              <h4 class="mb-3 mt-5">Creating Change, Starting Conversations</h4>
              <p>Yet, to date, traditional gender and behavioural stereotypes have seemingly prevailed, preventing open and honest conversations coming to the forefront of public campaigns. Men’s Health Week, held between 10th-16th June 2019, emphasised the key statistics that both men need to know about their health, and that policymakers need to know regarding how inequality and deprivation affect men’s health. </p>
            
            
            <p>Rising suicide rates over the past ten years have been a key indicator fuelling the concerns and calls for more to be done about men’s mental health. In the US in 2017, suicide was reported as the 10th leading cause of death. </p>
            <p>However, a shift is taking place as “the definition of masculinity is changing”,​ highlights Andrea Wroble, Health and Wellness Analyst at Mintel. As the movement towards transparent, open and authentic discussions on these topics ramps up, Andrea Wroble, Health and Wellness Analyst at Mintel reveals there are core areas where cosmetics and personal care brands are focusing their marketing and brand engagement efforts on.</p>
            
              <h4 class="mb-3 mt-5">A Shift in Communication</h4>
              <p>The media and entertainment industry are often perceived as contributing towards the continuation of these traditional gender roles through depicting men as “unable to discuss or process emotions”.​ In turn, where masculinity is defined as mental toughness, it has “created an environment where men’s emotional wellbeing silently suffers”​, Andrea Wroble, Health and Wellness Analyst at Mintel, states.</p>
                <p>As the landscape shows distinct efforts to move away from traditional male stereotype-based marketing, there are now growing opportunities for consumers and brands to challenge ideas of masculinity and actively remove the barriers that exist around these stereotypes. Increasingly, celebrities and male role models are openly talking about mental health, garnering more attention and support for a society where archaic views are dismantled and replaced with positive and progressive ones. Prince Harry and Michael Phelps, along with documentaries on the topic of suicide including Avicii: True Stories are all advocating for real change, and contributing to the male mental health discussion.</p>
              
              <p>Brands in the personal care space are taking notice. As a result, some are incorporating this changing and welcomed narrative into their storytelling efforts in a supportive and sensitive way. Roman is one such direct-to-consumer brand raising the profile of wellness amongst men. It is doing this by promoting open communication and providing access to medical advice and medication in a variety of areas relating to overall sexual health including erectile dysfunction and premature ejaculation.</p>
