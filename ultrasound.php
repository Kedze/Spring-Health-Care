						  	<h5><a href="#">Early Pregnancy Scan</a></h5>
						  	<p>Missed your period? An early pregnancy test from the time you have missed your period to 13 weeks will confirm if you are pregnant. An early pregnancy scan at Spring Health Care Ltd will help confirm the location of the pregnancy as well as the number of developing babies be it single or multiple pregnancy.The early pregnancy scan also confirms the viability of the pregnancy by locating the heart beat of your baby. Our scan will accurately predict your expected date of delivery from 8 to 13 weeks of pregnancy. Accurate dating in pregnancy is important to ensure optimal management by your obstetrician should complications arise during the pregnancy.</p>
							  <p>With the use of both our transvaginal and transabdominal probes, we are able to pick up life threatening ectopic pregnancies (pregnancy outside the womb) at a very early stage that may be amenable to medical treatment thus avoiding surgery. You do not need a referral or request to have your booking or early dating scans at Spring Health Care Ltd. Your printed results with an accompanying report will be given to you for antenatal care booking.</p>
						  </div>
						  <div class="tab-pane container p-0 fade" id="services-5">
						  	<div class="img" style="background-image: url(images/ultra_4.jpg);"></div>
						  	<h5><a href="#">Nuchal Scan</a></h5>
						  	<p>The 10 – 14 weeks Nuchal Translucency (NT) scan is a specialised obstetric scan exclusive to Spring Health Care Ltd. The NT scan helps assess your unborn baby’s risk of developing Down’s Syndrome and other genetic abnormalities. It is particularly important for:
                               <li>Women above 35years</li>
                               <li>Women with previous Down’s babies</li>
                               <li>Women who have had fertility treatment in terms of Invitro Fertilization (IVF)</li>
                               <li>Women who had uncontrolled diabetes before getting pregnant.</li>
                                Babies with Down’s Syndrome tend to accumulate more fluid at the back of the neck causing the nuchal space to be thicker than average. A positive nuchal translucency scan is followed by confirmatory laboratory tests. Nuchal scans can also determine if your baby has brain abnormalities such as absent skull bones or brain tissues.
                                You do not need a request from your obstetrician or midwife to have this scan done. Call us to book an appointment if you need to have a nuchal scan performed.
                              </p>
						  </div>
						  
						  <div class="tab-pane container p-0 fade" id="services-6">
						  	<div class="img" style="background-image: url(images/cervix.jpg);"></div>
						  	<h5><a href="#">Cervical Length Scan</a></h5>
						  	<p>This specialized obstetric scan performed at Spring Health Care Ltd from 10 weeks of pregnancy uses a transvaginal approach to assess the cervix for its length, the presence or absence of “funnelling” or dilation of the cervix; features which may indicate cervical incompetence. <br>Cervical incompetence refers to dilation of the cervix, without apparent uterine contractions; in the second or early third trimester of pregnancy. If left untreated it can result in preterm delivery and a premature baby. The shorter your cervical length, the greater the likelihood of cervical incompetence and preterm delivery.</p>
						  </div>
                            <div class="tab-pane container p-0 fade" id="services-7">
						  	<div class="img" style="background-image: url(images/ultra_6.jpg);"></div>
						  	<h5><a href="#">Anomaly Scan</a></h5>
						  	<p>The anomaly scan performed between 18 – 24 weeks is the most important milestone in your pregnancy. This is a highly specialised scan which involves meticulous scrutiny of the foetus. At Spring Health Care Ltd, we provide a detailed check of your unborn baby’s head, brain, face, neck, spine, chest, heart, abdomen, kidneys, bladder and all the limbs to assure you of normalcy.</p>
                                <p>The anomaly scan may also pick up the first signs of deformities in your baby. In addition, we assess your womb, placenta and cervix to rule out any abnormalities. We offer a detailed two-page report and are more than happy to discuss your scan results with you. We also offer a sneak peek of your baby in 4D and gender-check at no extra cost.</p>
						  </div>

						  <div class="tab-pane container p-0 fade" id="services-8">	  
						  	<div class="img" style="background-image: url(images/ultra_3.jpg);"></div>
						  	<h5><a href="#">Gender Identification Scan</a></h5>
						  	<p>Eager to know if your baby is a boy or girl? From as early as 16 weeks gestation, we can identify the gender of your baby at Spring Health Care Ltd. The best time for gender identification however is at the 20 weeks anomaly scan and beyond.<br> We improve the accuracy of diagnosis of the gender to up to 99% by investing enough time to obtain several different views between the foetal legs and buttocks. We also check on the well-being of your baby during the gender scan at no extra cost. You do not require a referral from your midwife or doctor to visit us.</p>
