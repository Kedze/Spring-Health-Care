						  	<h5><a href="#">Fetal Growth Scan</a></h5>
						  	<p>The foetal growth scan is also known as the foetal welfare, foetal well being or foetal biometry scan.
                                This scan allows estimation of the weight of your baby and enables us detect if your baby is not growing as expected. This information may be critical for decision-making by your doctor which could save your unborn baby’s life.
                              </p>
                              <p>The Welfare Scan is performed for high-risk pregnant women with hypertension, diabetes, sickle cell disease, cardiac disease, autoimmune disorders or thyroid disease.</p>
						  </div>
						  <div class="tab-pane container p-0 fade" id="services-10">
						  	<div class="img" style="background-image: url(images/pl.jpg);"></div>
						  	<h5><a href="#">Placenta Localization Scan</a></h5>
						  	<p>Placenta praevia refers to a placenta extending to or covering the internal cervical os (the inner entrance to the womb). When the placenta lies too low in the womb after 28 weeks of pregnancy, it is known as placenta praevia. There are various degrees of placenta praevia.</p>
                              <p>A placenta praevia is associated with increased risk of uncontrollable bleeding and if not diagnosed could lead to antepartum haemorrhage endangering the life of the baby and mother. </p>
						  </div>
						  <div class="tab-pane container p-0 fade" id="services-11">
						  	<div class="img" style="background-image: url(images/ultra_5.png);"></div>
						  	<h5><a href="#">Fetal Doppler Scan</a></h5>
						  	<p>Fetal doppler scans study the blood flow between you and your unborn baby to ensure your baby is getting all the required oxygen and nutrient supply necessary for a healthy growth.
                              </p>
                              <p>The introduction of fetal Dopplers in obstetric care have vastly improved outcomes of high risk pregnancies and reduced dramatically the incidence of intrauterine foetal deaths. Doppler ultrasound scans are essential i.e. if your baby is small for dates, you are carrying twins, you previously lost a baby in your womb (intrauterine foetal death), your baby is affected by rhesus antibodies or if there is oligohydramnios (markedly reduced fluid surrounding the baby).</p>
						  </div>
                            <div class="tab-pane container p-0 fade" id="services-12">
						  	<div class="img" style="background-image: url(images/4d.jpg);"></div>
						  	<h5><a href="#">Bonding with your baby in 4D</a></h5>
						  	<p>Thanks to our latest 4D ultrasound baby scan machine, you can take an amazing glimpse into the world of your unborn child between 18 – 28 weeks. You and your partner can have the pleasure of viewing your unborn baby’s movements such as smiling and sucking the thumbs in real time and colored video.</p>
                                <p>We can create an edited album for you and your loved ones to watch on your PC, IPhones/ Android phones. For expectant couples who wish to have a baby shower, we can produce beautiful 3D and 4D pictures for the occasion.</p>
						  </div>
						  <div class="tab-pane container p-0 fade" id="services-13">
						  	<div class="img" style="background-image: url(images/term.jpg);"></div>
						  	<h5><a href="#">Term Scan</a></h5>
						  	<p>The term scan, also known as the pre-birth scan is performed just before delivery. It is usually performed between 36 to 41 weeks and is crucial as it may detect abnormalities that may endanger the baby’s life during vaginal delivery and helps the doctor decide on the safest mode of delivery for both baby and mother.</p>
                             <p>The term scan confirms the lie of the baby, the presentation (whether head or legs are in your pelvic cavity), the weight of the baby, the amount of fluid around the baby and the level of the placenta in relation to the neck of the womb.
                                 The scan can also determine whether the baby's umbilical cord is around the neck and if so, how many loops of cord can be found around the baby's neck. The scan will also confirm the baby's heart beat.
                              </p> 
