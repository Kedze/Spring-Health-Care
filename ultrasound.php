						  	<h5><a href="#">Hystero-Sonosalpingography</a></h5>
						  	<p>Sono – HSG is a simple, well-tolerated ultrasound procedure used to evaluate the uterine cavity and patency of the fallopian tubes. It involves the instillation of saline and air through the cervical canal, followed by a Transvaginal scan. It detects uterine polyps, submucous fibroid and scarring of the lining of the womb that may interfere with fertility. It also detects blocked fallopian tubes in women who are being treated for infertility. If you are not getting pregnant, you may need to have a Sono – HSG.</p>
